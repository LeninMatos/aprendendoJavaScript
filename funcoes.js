//EXEMPLO 01
// 1) Declarar a função
function imprimeTexto(texto) {
    console.log(texto)
}

// 2) Executa a função uma ou mais vezes

imprimeTexto("oi");
imprimeTexto("olá");

//EXEMPLO 02 

function soma() {
    return 2 + 2; //tem que ser a última linha da função
}

console.log(soma())

imprimeTexto(soma())

